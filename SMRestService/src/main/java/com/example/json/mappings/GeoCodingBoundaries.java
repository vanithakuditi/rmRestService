package com.example.json.mappings;

public class GeoCodingBoundaries {
	 private GeoCodingLongLatitudes northeast;
	 private GeoCodingLongLatitudes southwest;
	/**
	 * @return the northeast
	 */
	public GeoCodingLongLatitudes getNortheast() {
		return northeast;
	}
	/**
	 * @param northeast the northeast to set
	 */
	public void setNortheast(GeoCodingLongLatitudes northeast) {
		this.northeast = northeast;
	}
	/**
	 * @return the southwest
	 */
	public GeoCodingLongLatitudes getSouthwest() {
		return southwest;
	}
	/**
	 * @param southwest the southwest to set
	 */
	public void setSouthwest(GeoCodingLongLatitudes southwest) {
		this.southwest = southwest;
	}
	 
	 
}
